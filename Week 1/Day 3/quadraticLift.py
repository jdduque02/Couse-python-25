def quadraticLift():
    """
    This function will calculate and print the quadratic lift of a number.
    The user will be asked to enter the value of 'a'.
    The function will then print the quadratic lift of 'a'.
    """
    a = float(input('Enter the value of a: '))
    return print(f'The quadratic lift is: {a**2}')
quadraticLift()