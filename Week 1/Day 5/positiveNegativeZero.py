def positiveNegativeZero():
    """
    Asks the user for a number, then prints a message to the console whether the number is positive, negative or zero.
    """
    
    number = float(input("Enter a number: "))
    if number > 0:
        return print("The number is zero")
    elif number < 0:
        return print("The number is negative")
    else:
        return print("The number is positive")
positiveNegativeZero()
